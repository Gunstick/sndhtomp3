#!/bin/bash
deperr=0
for dep in hatari-prg-args hatari sc68 readlink xdotool pacmd ffmpeg sox
do
  if ! which "$dep" >/dev/null
  then
    echo "ERROR cannot find $dep executable"
    deperr=1
  fi
done
if [ "$1" = "" ] || [ "$deperr" -eq 1 ]
then
  echo usage: $0 tune.sndh
  exit
fi
tunepath="$1" 
if [ ! -f "$tunepath" ]
then
  echo "file $tunepath not found"
  exit
else
  tunepath="$(readlink -f "$tunepath")"
  echo "converting $tunepath"
fi
shellpath="$(readlink -f "$0")"
tmp_dir=$(mktemp -d -t sndh-XXXXXXXXXX)
curr_dir="$(pwd)"
function errorout() {
  echo "$1"
  cd "$curr_dir"
  rm -rf "$tmp_dir" 
  if [ "$paindex" != "" ]
  then
    echo "unmuting hatari"
    pacmd set-sink-input-mute "$paindex" 0   # unmute hatari
  fi
  if [ "$hpid" != "" ]
  then
    echo "stop hatari"
    kill "$hpid"   # kill hatari
    kill -9 "$hpid" 2>/dev/null  # kill hatari
  fi
  exit 1
}
cd "$tmp_dir" || errorout "failure on temp dir"
trap "errorout aborted" 1 2 3 
tune="$(basename "$tunepath")"
if [ -f "t$$.snd" ]
then
  errorout "file t$$.snd exists in current directory, cannot continue"
fi
cp "$tunepath" "t$$.snd"
# get legth of tune
lensec=$(sc68 -n "t$$.snd"  | awk '$1=="Track" {m=$4;sub(":.*","",m);s=$4;sub(".*:","",s);print m*60+s}')
if [ "$lensec" -eq 180 ] 
then
  echo "unknowns tune length (default 180) extending to 300"
  lensec=300
fi
echo "tune length: $lensec s"
# extract snd_play
awk '/^# BEGIN BASE64 tar shd_play/{f=1;next}/^# END BASE64 tar shd_play/{f=0}f' "$shellpath" | base64 -d | tar xzf -
#launch tune in hatari
if [ -f snd_play.prg ] && [ -f snd_play.rsc ]
then
  :
else
  errorout "snd_play.prg or snd_play.rsc extraction error."
fi
echo "Running Hatari with snd_play.prg t$$.snd ($tune)"
if [ "$DEBUG" = "" ]
then
  hatari-prg-args --machine ste --fast-boot true --fast-forward true --sound 50066 --avirecord --avi-file "$tmp_dir"/out$$.avi --drive-a off --disable-video true --zoom 1 --borders off --confirm-quit false -- snd_play.prg "t$$.snd" >/dev/null 2>&1 &
else
  hatari-prg-args --machine ste --fast-boot true --sound 50066 --avirecord --avi-file "$tmp_dir"/out$$.avi --drive-a off --zoom 1 --borders off --confirm-quit false -- snd_play.prg "t$$.snd" &
fi

sleep 1   # wait for 'avi record started'
hpid=$(ps -edf|awk -v pid=$! '$3==pid {print $2}')
wid="$(xdotool search --pid "$hpid")"
if [ "$wid" = "" ]
then
  errorout "hatari not found"
fi
if [ "$DEBUG" = "" ]
then
  echo "Muting hatari ($hpid)"
  paindex="$(
  pacmd list-sink-inputs | 
    awk -v pid="$hpid" '
    $1 == "index:" {idx = $2} 
    $1 == "application.process.id" && $3 == "\"" pid "\"" {print idx; exit}
    ' )"
  pacmd set-sink-input-mute "$paindex" 1
fi
sleep 1
echo "launching player with [return]"
xdotool key -window $wid Return
if [ "$DEBUG" = "" ]
then
  echo "hiding hatari"
  xdotool windowunmap $wid
fi
echo "waiting $((lensec+20))s"
i=1
while [ $i -lt $((lensec+120)) ]
do
  sleep 1
  i=$((i+1))
  winname="$(xdotool getwindowname $wid 2>/dev/null)"
  if [ $? -ne 0 ]
  then
    echo "stopping, hatari dissapeared"
   # break
  fi
  if [ $(wc -c <<< $winname) -gt 6 ]
  then
    runsec=$(echo "$winname" | awk -F : 'NF==3 {print $1*60*60+$2*60+$3}')
    printf "%s/%s\r" "$runsec" "$lensec"
    if [ "$runsec" -gt "$((lensec+20))" ]
    then
      echo "running long enough"
      break
    fi
  fi
done
echo
echo "hatari F12"
xdotool key --window "$wid" F12   # pause hatari
sleep 3
if [ "$DEBUG" = "" ]
then
  echo "unmute hatari"
  pacmd set-sink-input-mute "$paindex" 0   # unmute hatari
  unset paindex
fi
sleep 1
echo "stop hatari"
kill "$hpid"   # kill hatari
kill -9 "$hpid" 2>/dev/null  # kill hatari
unset hpid
echo "Extracting audio from out$$.avi and deleting avi"
ffmpeg -i "$tmp_dir"/out$$.avi "$tmp_dir"/out$$.mp3 </dev/null >/dev/null 2>&1    # yeah, ffmpeg seems not to play nice in a shell
echo "trimming mp3 and creating "$(basename "$tunepath" .sndh)".mp3"
sox "$tmp_dir"/out$$.mp3 "$(basename "$tunepath" .sndh)".mp3 silence 1 0.1 1% trim 0 $((lensec+5)) 
mv -i "$(basename "$tunepath" .sndh)".mp3 "$curr_dir"/ || errorout "cannot move destination file"
echo "cleanup"
cd "$curr_dir"
rm -rf "$tmp_dir"
exit
